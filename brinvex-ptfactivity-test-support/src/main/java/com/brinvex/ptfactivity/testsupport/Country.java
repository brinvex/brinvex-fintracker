package com.brinvex.ptfactivity.testsupport;

/**
 * {@code Country} provides constant values for commonly used ISO 3166-1 alpha-2 country codes.
 * <p>
 * This interface is intended for use in tests.
 * </p>
 * <p>
 * Note that <i>Brinvex PtfActivity</i> supports any alpha-2 country code and is not limited to this or any other predefined list of countries.
 * </p>
 */
public interface Country {
    String US = "US";
    String DE = "DE";
    String CZ = "CZ";
}
