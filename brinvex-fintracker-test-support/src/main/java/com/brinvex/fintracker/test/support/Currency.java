package com.brinvex.fintracker.test.support;

/**
 * {@code Currency} provides constant values for commonly used ISO 4217 currency codes.
 * <p>
 * This interface is intended for use in tests.
 * </p>
 * <p>
 * Note that <i>Brinvex FinTracker</i> supports any ISO 4217 currency code and is not limited to this or any other predefined list of currencies.
 * </p>
 */
public interface Currency {
    String USD = "USD";
    String EUR = "EUR";
    String CZK = "CZK";
}
